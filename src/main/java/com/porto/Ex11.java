package com.porto;

public class Ex11 {
    public static void main(String[] args) {
        int[] inteiros = {10, 20, 30, 40, 50};
        int soma = 0;

        for (int inteiro : inteiros){
            soma += inteiro;
        }
        System.out.println(soma);
    }
}

